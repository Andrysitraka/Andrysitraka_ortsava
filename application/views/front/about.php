      <h4>Potentiel Agricole</h4>
  </br>
      <p style="text-align:justify"> 
      	La superficie agricole exploitée dans la région actuellement reste faible. La SAVA dispose ainsi d’une
      	 réserve de terrain arable qui mérite d’être exploitée. Dans l’ensemble de la région, la superficie 
      	 exploitée ne représente que moins de 15 % de la superficie cultivable. La spécificité du climat est
      	  propice à la pratique de cultures d’exportation telles que la vanille ou le girofle. La région représente
      	   actuellement une part importante de la production de vanille de Madagascar et cette filière mérite d’être
      	    développée continuellement. Toutefois, les services de proximité de vulgarisation et d’encadrement restent
      	     encore à un niveau assez faible et méritent d’être développés. En effet, le développement de chaque filière
      	      nécessite l’amélioration des services connexes. Par ailleurs, des efforts
      	 doivent être considérés pour le renforcement de capacités des paysans et tendre ainsi à la
      	  professionnalisation de ceux-ci à moyen terme
	</p>
