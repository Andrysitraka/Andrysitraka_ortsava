      	La région dispose de nombreux sites qui permettent de développer facilement ce secteur.
      	 La couverture des opérateurs de téléphonie mobile dans la région ne fait que favoriser 
      	 le développement de ce secteur. Actuellement, la région dispose aussi d’un office de 
      	 tourisme qui est en charge de faire la promotion de la région au niveau des différents
      	  acteurs et de faciliter les activités de ces derniers. Toutefois, le développement du 
      	  tourisme est freiné par certains facteurs tels que l’accessibilité de la région et aussi
      	   aux différent sites touristiques, l’inexistence de produits touristiques bien définis à
